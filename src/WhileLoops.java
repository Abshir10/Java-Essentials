public class WhileLoops {

    public static void main(String[] args) {
        int i =0;
        while (i <5 ) {
            System.out.println(i);
            i = i+1;
        }
        int x =5 ;
        do {
            System.out.println(x);
            x = x-1;

        } while (x>0);
    }
}
